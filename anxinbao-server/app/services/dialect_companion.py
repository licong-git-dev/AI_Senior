# ==================== 方言陪伴服务 ====================

class DialectCompanionService:
    """方言情感陪伴服务 — 核心卖点实现"""

    # 鄂州话用户优先回退到武汉话（咸宁/黄石片区方言相近，避免占位模板伤害体验）
    _DIALECT_FALLBACK = {"ezhou": "wuhan"}

    def _resolve_dialect(self, dialect: str, table: Dict) -> str:
        """根据模板覆盖度智能回退方言"""
        if dialect in table:
            return dialect
        fallback = self._DIALECT_FALLBACK.get(dialect)
        if fallback and fallback in table:
            return fallback
        return "mandarin"

    def get_greeting(
        self,
        dialect: str = "mandarin",
        name: str = "您",
        weather: str = "晴天",
        activity: str = "出门散步",
        hour: Optional[int] = None
    ) -> str:
        """获取方言问候语"""
        if hour is None:
            hour = datetime.now().hour

        if 5 <= hour < 11:
            period = "morning"
        elif 11 <= hour < 18:
            period = "afternoon"
        elif 18 <= hour < 22:
            period = "evening"
        else:
            period = "night"

        d = self._resolve_dialect(dialect, DIALECT_GREETINGS)
        templates = DIALECT_GREETINGS[d]
        greetings = templates.get(period, templates.get("morning", []))

        if not greetings:
            return f"{name}，您好！"

        template = random.choice(greetings)
        return template.format(
            name=name,
            weather=weather,
            activity=activity
        )

    def get_emotion_response(
        self,
        emotion: str,
        dialect: str = "mandarin",
        name: str = "您",
        child_name: str = "孩子"
    ) -> str:
        """根据情绪获取方言安慰/回应"""
        d = self._resolve_dialect(dialect, DIALECT_EMOTION_RESPONSES)
        templates = DIALECT_EMOTION_RESPONSES[d]
        responses = templates.get(emotion, templates.get("lonely", []))

        if not responses:
            return f"{name}，我在这里陪着您。"

        template = random.choice(responses)
        return template.format(name=name, child_name=child_name)

    def get_food_therapy(
        self,
        condition: str,
        dialect: str = "mandarin",
        name: str = "您"
    ) -> str:
        """获取方言食疗建议"""
        d = self._resolve_dialect(dialect, DIALECT_FOOD_THERAPY)
        templates = DIALECT_FOOD_THERAPY[d]
        suggestions = templates.get(condition, templates.get("general", []))

        if not suggestions:
            return f"{name}，注意饮食健康。"

        template = random.choice(suggestions)
        return template.format(name=name)

    def get_care_reminder(
        self,
        reminder_type: str,
        dialect: str = "mandarin",
        name: str = "您",
        **kwargs
    ) -> str:
        """获取方言关怀提醒"""
        d = self._resolve_dialect(dialect, DIALECT_CARE_REMINDERS)
        templates = DIALECT_CARE_REMINDERS[d]
        reminders = templates.get(reminder_type, [])

        if not reminders:
            return f"{name}，请注意身体健康。"

        template = random.choice(reminders)
        return template.format(name=name, **kwargs)

    def get_festival_blessing(
        self,
        festival: str,
        dialect: str = "mandarin",
        name: str = "您",
    ) -> str:
        """获取方言节日祝福（spring_festival / mid_autumn / dragon_boat 等）"""
        d = self._resolve_dialect(dialect, DIALECT_FESTIVAL_BLESSINGS)
        templates = DIALECT_FESTIVAL_BLESSINGS[d]
        blessings = templates.get(festival, [])

        if not blessings:
            return f"{name}，节日快乐！"

        return random.choice(blessings).format(name=name)

    def stats(self) -> Dict[str, int]:
        """模板覆盖度统计（用于运营/产品看板）"""
        def _count(table: Dict) -> Dict[str, int]:
            return {
                dialect: sum(len(v) for v in cats.values())
                for dialect, cats in table.items()
            }
        return {
            "greetings": _count(DIALECT_GREETINGS),
            "emotions": _count(DIALECT_EMOTION_RESPONSES),
            "food_therapy": _count(DIALECT_FOOD_THERAPY),
            "care_reminders": _count(DIALECT_CARE_REMINDERS),
            "festival_blessings": _count(DIALECT_FESTIVAL_BLESSINGS),
        }


